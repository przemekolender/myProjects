public class Tuple3 {
    private int x;
    private int y;
    private boolean ver;

    Tuple3(int x, int y, boolean ver){
        this.x = x;
        this.y = y;
        this. ver = ver;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public boolean isVer() {
        return ver;
    }
}
